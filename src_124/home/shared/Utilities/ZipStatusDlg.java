    @Override
    public void new_status(int code, String st)
    {
        last_code = code;
        if (last_status == null || last_status.compareTo(st) != 0)
        {
            last_status = st;
            if (code == ZipListener.ST_ERROR)
            {
                BT_ACTION.setText("Close");
                sb.append( "ERROR:\n");
            }

            if (code == ZipListener.ST_READY)
            {
                BT_ACTION.setText("Ready");
                BT_ACTION.setEnabled(true);
            }
            
            sb.append( st + "\n");
            String txt = sb.toString();
            TXTA_TERMINAL.setText( txt );
            // SET CARET TO END OF TEXT -> SCROLL
            TXTA_TERMINAL.setCaretPosition(txt.length());
        }
    }    
    public void add_description( String txt )
    {
        String new_text = TXTA_DESCRIPTION.getText();
        if ( new_text.length() > 0)
             new_text += "\n";
       if (txt != null)
             new_text += txt;
        
        TXTA_DESCRIPTION.setText(  new_text);
    }
    public void set_description(String new_text)
    {
        TXTA_DESCRIPTION.setText(  new_text);
    }

    @Override
    public void actionPerformed(ActionEvent e)
    {
        auto_close_cnt--;
        BT_ACTION.setText("Ready (" + auto_close_cnt + ")");
        if (auto_close_cnt <= 0 && isVisible())
        {
            setVisible(false);
            finished = true;
        }  
    }
    
